package design_problems.jira_clone.domain;

import java.util.List;

public class User {
    private String name;
    private List<Task> tasks;

    public User(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "name='" + name + '\'' +
                '}';
    }
}
