package design_problems.parking_lot;

/**
 * This enum represents the type of parking slot.
 * It can be SMALL, MEDIUM, or LARGE.
 * The size of the parking slot depends on the type of vehicle.
 * SMALL for bikes, MEDIUM for cars, and LARGE for trucks.
 */
public enum ParkingSlotType {
    SMALL,
    MEDIUM,
    LARGE
}
